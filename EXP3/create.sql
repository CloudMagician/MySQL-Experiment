CREATE DATABASE SCHOOL;
USE SCHOOL;

CREATE TABLE STUDENT(
	SNO VARCHAR(5) PRIMARY KEY,
	SNAME VARCHAR(10),
	SSEX VARCHAR(6),
	SAGE NUMERIC(3,0),
	SDEPT VARCHAR(20),
	CHECK (SSEX IN ('male','female')),
	CHECK (SAGE >= 18),
	CHECK (SAGE <= 22)
);
	
CREATE TABLE COURSE(
	CNO VARCHAR(5) PRIMARY KEY,
	CNAME VARCHAR(55),
	CTIME NUMERIC(3,0),
	CCREDIT  NUMERIC(1,0)
);

CREATE TABLE SC(
	SNO VARCHAR(5),
	CNO VARCHAR(5),
	GRADE NUMERIC(3,0),
	FOREIGN KEY (SNO) REFERENCES STUDENT(SNO),
	FOREIGN KEY (CNO) REFERENCES COURSE(CNO) 
);